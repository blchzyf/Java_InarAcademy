package chapters.chapter_03.Checkpoints;

public class Checkpoint02 {
    public static void main(String[] args) {

        int x =1;

        System.out.println("x>1 is " + (x>0));      //true
        System.out.println("x<1 is " + (x<0));      //false
        System.out.println("x!=1 is " + (x!=0));    //true
        System.out.println("x>=1 is " + (x>=0));    //true
        System.out.println("x!=1 is " + (x!=1));    //false

    }
}
