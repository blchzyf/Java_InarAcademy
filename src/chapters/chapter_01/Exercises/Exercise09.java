package chapters.chapter_01.Exercises;

public class Exercise09 {
    public static void main(String[] args) {

        double width = 4.5;
        double height = 7.9;
        double area = width * height;
        double perimeter = 2* (width+height);

        System.out.println("Perimeter of a rectangle that has width 4.5 and height 7.9: " +perimeter);
        System.out.println("Area of a rectangle that has width 4.5 and height 7.9: " +area);
    }
}
